from typing import List
from collections import defaultdict

class Solution:
    def threeSumClosest(self, nums: List[int], target: int) -> int:
        map = defaultdict(int)
        for i in range(0, len(nums)-2):
            x = len(nums)-1
            y = x-1
            while y < x:
                y = x-1
                while i < y:
                    curr = nums[i] + nums[x] + nums[y]
                    res = abs(curr - target)
                    map[curr] = res
                    # res.append(curr)
                    y -= 1
                x -= 1

        return min(map, key=map.get)
